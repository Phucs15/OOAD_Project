-- Supplier Table
CREATE TABLE Supplier (
    Supplier_ID INT PRIMARY KEY,
    Supplier_Name VARCHAR(100),
    Contact_Number VARCHAR(20),
    Address VARCHAR(100)
);

-- Shipment Table
CREATE TABLE Shipment (
    Shipment_ID INT PRIMARY KEY,
    Delivery_Status VARCHAR(50),
    Recipient_Date DATE,
    Delivery_Date DATE,
);

-- Customer Table
CREATE TABLE Customer (
    Customer_ID INT PRIMARY KEY,
    First_Name VARCHAR(50),
    Last_Name VARCHAR(50),
    Customer_Name VARCHAR(100),
    Phone_Number VARCHAR(20),
    Address VARCHAR(100),
	Address_Region NVARCHAR(255)
);

-- Warehouse Table
CREATE TABLE Warehouse (
    Warehouse_ID INT PRIMARY KEY,
    Warehouse_Name NVARCHAR(255), 
    Latitude DECIMAL(10, 6),
    Longitude DECIMAL(10, 6),
    Capacity INT
);

-- Order Table
CREATE TABLE Orders (
    Order_ID INT PRIMARY KEY,
    Customer_ID INT,
	Shipment_ID INT,
    Order_Date DATE,
    Expected_Delivery DATE,
    FOREIGN KEY (Shipment_ID) REFERENCES Shipment(Shipment_ID),
	FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID)
);

-- Receives (Many-to-Many relationship between Warehouse and Order)
CREATE TABLE Receives (
    Order_ID INT,
    Warehouse_ID INT,
    PRIMARY KEY (Order_ID, Warehouse_ID),
    FOREIGN KEY (Order_ID) REFERENCES Orders(Order_ID),
    FOREIGN KEY (Warehouse_ID) REFERENCES Warehouse(Warehouse_ID)
);

-- Transaction Table
CREATE TABLE Transactions (
    Transaction_ID INT PRIMARY KEY,
    Customer_ID INT,
    Transaction_Date DATE,
    Transaction_time TIME,
    Transaction_status VARCHAR(50),
    Payment_method VARCHAR(50),
    Amount DECIMAL(10, 2),
    FOREIGN KEY (Customer_ID) REFERENCES Customer(Customer_ID)
);

-- Pay Table (Many-to-Many relationship between Order and Transaction)
CREATE TABLE Pay (
    Transaction_Amount DECIMAL(10, 2),
    Order_ID INT,
    Transaction_ID INT,
    PRIMARY KEY (Order_ID, Transaction_ID),
    FOREIGN KEY (Order_ID) REFERENCES Orders(Order_ID),
    FOREIGN KEY (Transaction_ID) REFERENCES Transactions(Transaction_ID)
);

-- Employee Table
CREATE TABLE Employee (
    Employee_ID INT PRIMARY KEY,
    First_Name VARCHAR(50),
    Last_Name VARCHAR(50),
    Employee_Name VARCHAR(100),
    Phone_Number VARCHAR(20),
    Email VARCHAR(50),
    Address VARCHAR(100),
    Hire_Date DATE
);

-- Work Table (Many-to-Many relationship between Employee and Warehouse)
CREATE TABLE Work (
    Employee_ID INT,
    Warehouse_ID INT,
    PRIMARY KEY (Employee_ID, Warehouse_ID),
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID),
    FOREIGN KEY (Warehouse_ID) REFERENCES Warehouse(Warehouse_ID)
);

-- Manager Table (ISA relationship with Employee)
CREATE TABLE Manager (
    Manager_ID INT PRIMARY KEY,
    Managed_Department VARCHAR(50),
    Management_Level VARCHAR(50),
    Salary DECIMAL(10, 2),
    Bonus DECIMAL(10, 2),
    Direct_Reports INT,
    FOREIGN KEY (Manager_ID) REFERENCES Employee(Employee_ID)
);

-- Staff Table (ISA relationship with Employee)
CREATE TABLE Staff (
    Staff_ID INT PRIMARY KEY,
    Position VARCHAR(50),
    Shift VARCHAR(20),
    Department VARCHAR(50),
    Hourly_Rate DECIMAL(10, 2),
	Manager_ID INT,
	FOREIGN KEY (Manager_ID) REFERENCES Manager(Manager_ID),
    FOREIGN KEY (Staff_ID) REFERENCES Employee(Employee_ID)
);

-- Perform Table (Many-to-Many relationship between Employee and Order)
CREATE TABLE Perform (
    Employee_ID INT,
    Order_ID INT,
    Role_in_Order VARCHAR(50),
    Assigned_Date DATE,
    PRIMARY KEY (Employee_ID, Order_ID),
    FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID),
    FOREIGN KEY (Order_ID) REFERENCES Orders(Order_ID)
);

-- Product Table
CREATE TABLE Product (
    Product_ID INT PRIMARY KEY,
    Product_Name VARCHAR(100),
    Category VARCHAR(50),
    Price DECIMAL(10, 2),
	Supplier_ID INT,
    Description VARCHAR(255),
	Weight DECIMAL(10, 2),
	FOREIGN KEY (Supplier_ID) REFERENCES Supplier(Supplier_ID)
);

-- Order_Detail Table
CREATE TABLE Order_Detail (
    Detail_ID INT PRIMARY KEY,
    Order_ID INT,
    Product_ID INT,
    Quantity INT,
    Unit_Price DECIMAL(10, 2),
	Shipment_Location NVARCHAR(255),
    FOREIGN KEY (Order_ID) REFERENCES Orders(Order_ID),
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID)
);

-- Store Table (Many-to-Many relationship between Product and Warehouse)
CREATE TABLE Store (
    Warehouse_ID INT,
    Product_ID INT,
    Quantity INT,
    Last_Updated DATE,
    PRIMARY KEY (Warehouse_ID, Product_ID),
    FOREIGN KEY (Warehouse_ID) REFERENCES Warehouse(Warehouse_ID),
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID)
);

-- Pack Table (Many-to-Many relationship between Product and Order)
CREATE TABLE Pack (
    Product_ID INT,
    Order_ID INT,
    Quantity INT,
    PRIMARY KEY (Product_ID, Order_ID),
    FOREIGN KEY (Product_ID) REFERENCES Product(Product_ID),
    FOREIGN KEY (Order_ID) REFERENCES Orders(Order_ID)
);

